    /**
     * Introduce la informacion en un jTable indicado
     * @throws SQLException 
     */
    public void getTable() throws SQLException{
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        //System.out.println("getTable"); //cada vez que es llamado el metodo se mostrara
        String[] cols = {"id", "Aula"};
        int count = aula.getRegistros();
        String[][] data = new String[count][2];
        for (int i = 0; i < count; i++){
            for (int j = 0; j < 2; j++) {
                data[i][j] = aula.getAulas()[i][j];
            }
        }
        model.setDataVector(data, cols);
    }
    
    /**
     * Se hace uso de un comboBox con informacion oculta
     * este metodo agrega datos a un ComboBox para elminar el registro por su id
     */
    public void agregarAulaCombo() throws SQLException{
        HashMap<String, Integer> map = aula.primerCombo();              //obtiene los datos 
        Map<String, Integer> map1 = new TreeMap<String, Integer>(map);  //ordena el HashMap
        jComboBox1.removeAllItems();
        jComboBox2.removeAllItems();
        for(String s: map1.keySet()){   // agrega los datos al comboBox
            jComboBox1.addItem(s);
            jComboBox2.addItem(s);
        }
    }

    /**
     * Permite ordenar solo la columna de aulas en el jTable indicado
     */
    public void ordenarDatosTabla(){
        jTable1.setAutoCreateRowSorter(true);
        TableRowSorter<TableModel> sorter = new TableRowSorter<>(jTable1.getModel());
        jTable1.setRowSorter(sorter);
        sorter.sort();
        sorter.setSortable(0, false);
    }
