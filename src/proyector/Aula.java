    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
    try {
        HashMap<String, Integer> map = aula.primerCombo();
        //lblSeleccion.setText(map.get(jComboBox1.getSelectedItem().toString()).toString());
        int seleccion = map.get(jComboBox2.getSelectedItem().toString());
        if (!txtAulaNuevo.getText().trim().isEmpty()){
            aula.updAula(seleccion, txtAulaNuevo.getText());    //metodo actualizar
            dlgActualizar.setVisible(false);    //cerrar dialog
            dlgActualizar.dispose();            //cerrar dialog
            pnlCombo.setVisible(true);          //panel del comboBox
            pnlFormulario.setVisible(false);    //panel del formulario
            btnActualizar.setEnabled(false);    //boton actualizar
            getTable();
        }else{
            JOptionPane.showMessageDialog(null, "Error no puede dejar la casilla en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (SQLException ex) {
        Logger.getLogger(Aula.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

