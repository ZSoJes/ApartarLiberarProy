    private void btnEliminarRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarRegistroActionPerformed
        int opc = JOptionPane.showConfirmDialog(null, "SI HACE ESTO SE BORRARAN TODOS LOS REGISTROS QUE SE HICIERON CON ESTE AULA!!!", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE, JOptionPane.YES_NO_OPTION);
        if(opc == JOptionPane.YES_OPTION){
            try {
                HashMap<String, Integer> map = aula.primerCombo();
                //lblSeleccion.setText(map.get(jComboBox1.getSelectedItem().toString()).toString());
                int seleccion = map.get(jComboBox1.getSelectedItem().toString());
                PrestamoDB prestamo = new PrestamoDB();
                if(prestamo.getPrestamoActivo(String.valueOf(seleccion), 1)){
                    JOptionPane.showMessageDialog(null, "Los datos de han eliminado");
                    aula.destroyAula(seleccion);
                    dlgBorrar.setVisible(false);
                    dlgBorrar.dispose();
                    getTable();
                }else{
                    JOptionPane.showMessageDialog(null, "No se puede eliminar!!!\nEl aula que desea eliminar se encuentra registrada en un prestamo de Videoproyector\nprimero registre la devolucion del Videoproyector\ndespues proceda a eliminar el aula\n\nRECUERDE QUE ESTA ACCION ELIMINARA TODOS LOS REGISTROS RELACIONADOS");
                }
            } catch (SQLException ex) {
                Logger.getLogger(Aula.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No se ha borrado el registro\n\nRecuerde que esta accion es irreversible y si es ejecutada\nse borraran todos los registros relacionados a este aula", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarRegistroActionPerformed

