    public void darAcceso(){
        String usuario = txtUsuario.getText().trim();
        try {
            LeerInicio leer = new LeerInicio();
            try {
                if (leer.getExisteUsuario(usuario)) {
                    String hashed = leer.getPass(usuario);
                    if (BCrypt.checkpw(String.valueOf(jPassAcceso.getPassword()), hashed)) {
                        if(leer.getEsAdminUsuario(usuario)){
                            Menu menu = new Menu();
                            menu.setVisible(true);
                            this.setVisible(false);
                            this.dispose();
                            System.out.println("\nAcceso valido...\n\n");
                        }else{
                            throw new Exception();
                        }
                    } else {
                        throw new Exception();
                    }
                } else {
                    throw new Exception();
                }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Intenta de nuevo\nEsta ingresando icorrectamente \nusuario y/o contraseña!!!\n\no su usuario no puede dar inicio al programa", "Error", JOptionPane.ERROR_MESSAGE);
                txtUsuario.setText("");
                jPassAcceso.setText("");
            }
        } catch (SQLException ex) {
            Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void limiteYcaracteres(JTextField nombre, int limite, java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar(); //probar introducir solo caracteres
        String cad = ("" + c).toUpperCase();
        c = cad.charAt(0);
        evt.setKeyChar(c);
        if (!(Character.isAlphabetic(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE) || (c == KeyEvent.VK_SPACE))) {
            evt.consume();
        }
        if (nombre.getText().length() >= limite) {
            evt.consume();
        }
    }

