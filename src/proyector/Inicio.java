    private void btnCrearUsuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCrearUsuarioMouseClicked
        btnCrearUsuario.setBackground(new Color(239,239,239));
        lblBtnCrear.setForeground(new Color(100, 100, 100));

        String nom = txtNom.getText().trim().toUpperCase();
        String apat = txtAPat.getText().trim().toUpperCase();
        String amat = txtAMat.getText().trim().toUpperCase();
        char[] pass = jPass.getPassword();
        char[] passC = jPassConfirm.getPassword();
        String cred = txtAcceso.getText().trim();
        
        //revisar las contraseñas no esten en blanco
        if (!String.valueOf(pass).trim().isEmpty() && !String.valueOf(passC).trim().isEmpty()) {
            if (pass.length >= 8 ){
                
            
            //revisar ambas contraseñas sean iguales
            if (Arrays.equals(pass, passC)) {
                //revisar los cuadros de texto no esten en blanco
                if (!nom.isEmpty() && !(apat.length() < 2) && !amat.isEmpty()) {
                    try {
                        CrearInicio crear = new CrearInicio();
                        String hashed = BCrypt.hashpw(String.valueOf(pass).trim(), BCrypt.gensalt(12));  //cifrado
                        String datos[] = {nom, apat, amat, hashed};      // asigna para el metodo setUsuario
                        //revisar si el checkbox fue seleccionado
                        if (jCheckBoxAcceso.isSelected()) {
                            //revisar si el cuadro de acceso no esta en blanco
                            if (!cred.isEmpty()) {
                                System.out.println("id:" + cred);
                                crear.setUsuario(datos, true, cred);
                                GenerarReportes g = new GenerarReportes();
                                g.credencialUsuario(cred);
                                
                                lanzarRegistro();

                                //CERRAR DIALOG
                                dialogUsuario.setVisible(false);
                                dialogUsuario.dispose();
                            } else {
                                txtAcceso.setText("");
                                JOptionPane.showMessageDialog(null, "Desmarque la casilla anterior\nsi no desea usar una credencial existente", "Información", JOptionPane.INFORMATION_MESSAGE);
                            }
                        } else {
                            String id = crear.setUsuario(datos, true);
                            if(!id.isEmpty()){
                                System.out.println("se va a generar credencial");
                                GenerarReportes g = new GenerarReportes();
                                g.credencialUsuario(id);
                                
                                lanzarRegistro();

                                //CERRAR DIALOG
                                dialogUsuario.setVisible(false);
                                dialogUsuario.dispose();
                            }
                        }

                    } catch (SQLException ex) {
                        Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Debe llenar completamente el formulario", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ambas contraseñas deben coincidir", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
            
            }else{
                JOptionPane.showMessageDialog(null, "La contraseña ideal:\nal menos 8 caracteres sin datos personales", "Advertencia", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "No puede dejar las contraseñas en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        }
        // cerrar ventana

        //LIMPIAR CUADROS DE TEXTO
        txtNom.setText("");
        txtAPat.setText("");
        txtAMat.setText("");
        jPass.setText("");
        jPassConfirm.setText("");
    }//GEN-LAST:event_btnCrearUsuarioMouseClicked

    
